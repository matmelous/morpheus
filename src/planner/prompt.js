function buildPlanSchema(runnerKinds) {
  const supported = Array.isArray(runnerKinds) && runnerKinds.length
    ? runnerKinds.filter(Boolean).map((k) => String(k))
    : ['codex-cli', 'gemini-cli', 'claude-cli', 'cursor-cli', 'desktop-agent', 'auto'];

  const runKinds = supported.filter((k) => k !== 'auto');
  const setRunnerKinds = supported.includes('auto') ? supported : [...supported, 'auto'];

  const runKindsText = runKinds.map((k) => `"${k}"`).join(' | ');
  const setRunnerKindsText = setRunnerKinds.map((k) => `"${k}"`).join(' | ');

  return `{
  "version": 1,
  "action": "run" | "reply" | "set_project" | "set_runner" | "set_orchestrator" | "set_task_policy" | "memory_append" | "memory_set" | "memory_clear" | "memory_show" | "project_add" | "project_mkdir" | "project_clone" | "project_scan" | "longrun_initiate" | "longrun_gather" | "longrun_confirm" | "longrun_execute",

  // when action = "reply"
  "reply_text": string,

  // when action = "run"
  "runner_kind": ${runKindsText},
  "prompt": string,

  // when action = "set_project"
  "project_id": string,
  "create_new_task"?: boolean,

  // when action = "set_runner"
  "runner_kind": ${setRunnerKindsText},
  "scope"?: "task" | "user" | "global",

  // when action = "set_orchestrator"
  "provider": "gemini-cli" | "openrouter" | "auto",
  "scope"?: "user" | "global",

  // when action = "set_task_policy"
  "task_id_length"?: number,
  "project_task_history_limit"?: number,

  // when action = "project_add"
  "id": string,
  "cwd": string,
  "type"?: string,
  "name"?: string,

  // when action = "project_mkdir"
  "id": string,
  "dir": string,
  "type"?: string,
  "name"?: string,

  // when action = "project_clone"
  "id": string,
  "git_url": string,
  "dir"?: string,
  "depth"?: number,
  "type"?: string,
  "name"?: string,

  // when action = "memory_append" | "memory_set"
  "memory_text": string,
  "memory_scope"?: "user" | "project", // default: "user"

  // when action = "longrun_initiate"
  "feature_uuid": string,  // UUID v4 generated by the planner — will become the folder name
  "reply_text": string,    // first set of questions for the user

  // when action = "longrun_gather"
  "spec_json": string,     // JSON-stringified partial spec with all known data so far
  "reply_text": string,    // next questions or prompts for the user

  // when action = "longrun_confirm"
  "spec_json": string,     // JSON-stringified COMPLETE spec (all levels fully defined)
  "reply_text": string,    // formatted human-readable summary for user review

  // when action = "longrun_execute"
  // (no extra fields required — session is resolved from DB in inbound-core)

  // optional
  "title"?: string
}`;
}

function formatContext(messages) {
  if (!Array.isArray(messages) || messages.length === 0) return '(sem contexto anterior)';

  const lines = [];
  for (const m of messages) {
    const role = m.role || 'user';
    const content = String(m.content || '').trim();
    if (!content) continue;
    lines.push(`[${role}] ${content}`);
  }

  return lines.length > 0 ? lines.join('\n') : '(sem contexto anterior)';
}

function formatProjects(projects) {
  if (!Array.isArray(projects) || projects.length === 0) return '(nenhum projeto carregado)';
  const max = 25;
  const lines = [];
  for (const p of projects.slice(0, max)) {
    const id = p?.id || '';
    const name = p?.name || '';
    if (!id) continue;
    lines.push(`- ${id}${name && name !== id ? ` (${name})` : ''}`);
  }
  const extra = projects.length > max ? `\n(+${projects.length - max} outros)` : '';
  return lines.join('\n') + extra;
}

function formatGlobalChatHistory(entries) {
  if (!Array.isArray(entries) || entries.length === 0) return '(sem historico global)';

  const rows = [];
  const ordered = entries.slice().reverse();
  for (const item of ordered) {
    const role = String(item?.role || '').trim().toLowerCase() || 'assistant';
    const projectId = String(item?.project_id || '').trim();
    const content = String(item?.content || '').trim();
    if (!content) continue;
    const summary = String(item?.action_summary || '').trim();
    const summarySuffix = summary ? ` | action: ${summary.slice(0, 220)}` : '';
    rows.push(`[${role}${projectId ? ` @${projectId}` : ''}] ${content.slice(0, 420)}${summarySuffix}`);
  }

  return rows.length > 0 ? rows.join('\n') : '(sem historico global)';
}

function formatRunnerCatalog(runnerCatalog, fallbackRunnerKinds) {
  const kinds = Array.isArray(fallbackRunnerKinds)
    ? fallbackRunnerKinds.filter(Boolean).map((k) => String(k))
    : [];

  const list = Array.isArray(runnerCatalog) ? runnerCatalog : [];
  if (list.length === 0) {
    return kinds.length ? kinds.map((k) => `- ${k}`).join('\n') : '(catalogo indisponivel)';
  }

  const lines = [];
  for (const item of list.slice(0, 20)) {
    const kind = String(item?.kind || '').trim();
    if (!kind) continue;

    const planner = item?.planner && typeof item.planner === 'object' ? item.planner : null;
    const purpose = planner?.purpose ? String(planner.purpose).trim() : '';
    lines.push(`- ${kind}${purpose ? `: ${purpose}` : ''}`);

    const whenToUse = Array.isArray(planner?.whenToUse) ? planner.whenToUse : [];
    if (whenToUse.length > 0) {
      lines.push(`  quando_usar: ${whenToUse.join(' | ')}`);
    }

    const promptRules = Array.isArray(planner?.promptRules) ? planner.promptRules : [];
    if (promptRules.length > 0) {
      lines.push(`  prompt_rules: ${promptRules.join(' | ')}`);
    }

    const promptExamples = Array.isArray(planner?.promptExamples) ? planner.promptExamples : [];
    if (promptExamples.length > 0) {
      lines.push(`  prompt_examples: ${promptExamples.join(' | ')}`);
    }
  }

  return lines.length > 0 ? lines.join('\n') : '(catalogo indisponivel)';
}

