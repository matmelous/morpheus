    '- Se pedir modificacoes: use action="longrun_gather" com spec_json atualizado',
    '- NUNCA inicie execucao sem confirmacao explicita do usuario',
    '',
    'Formato do spec_json (estrutura completa):',
    '{ "feature": { "uuid": "...", "title": "...", "description": "..." },',
    '  "waves": [{ "uuid": "...", "title": "...", "order": 1, "parallel": false,',
    '    "epic_groups": [{ "uuid": "...", "title": "...", "description": "...",',
    '      "epics": [{ "uuid": "...", "title": "...", "description": "...",',
    '        "validation_instructions": "Run: npm test. Criterios: ...",',
    '        "tasks": [{ "uuid": "...", "title": "...", "description": "..." }]',
    '      }]',
    '    }]',
    '  }]',
    '}',
    '',
